void	ft_rev_int_tab(int *tab, int size)
{
	int c;
	int tmp;

	c = 0;
	while (c < size / 2)
	{
		tmp = tab[c];
		tab[c] = tab[(size - 1) - c];
		tab[(size - 1) - c] = tmp;
		c++;
	}
}

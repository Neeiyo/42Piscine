char	*ft_strcapitalize(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if ((str[a] >= 'A') && (str[a] <= 'Z'))
		{
			str[a] = str[a] + 32;
		}
		if (!((str[a - 1] >= 'a' && str[a - 1] <= 'z')
					|| (str[a - 1] >= 'A' && str[a - 1] <= 'Z')
					|| (str[a - 1] >= '0' && str[a - 1] <= '9')))
		{
			if (str[a] >= 'a' && str[a] <= 'z')
				str[a] = str[a] - 32;
		}
		a++;
	}
	return (str);
}

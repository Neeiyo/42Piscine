char	*ft_strcpy(char *dest, char* src)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while(src[y])
		y++;
	while(i < y)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}

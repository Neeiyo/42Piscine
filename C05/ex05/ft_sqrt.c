int		ft_sqrt(int nb)
{
	long int i;

	i = nb;
	if (nb <= 0)
		return (0);
	while ((i / 2 * i / 2) > nb)
		i = i / 2;
	while (i * i > nb)
		i--;
	if (i * i == nb)
		return (i);
	else
		return (0);
}
